piano_keys = ["a","b","c","d","e","f","g"]

print(piano_keys[2:5])

print(piano_keys[2:]) #from slice 2 to the end

print(piano_keys[:5]) # from the start to the last slice

print(piano_keys[2:5:2]) # increasing 2 from slice 2 to 5

print(piano_keys[::2]) #print every 2 slices

print(piano_keys[::-1]) # revers a list